int
sys_fork(void)
{
  return fork();
}

int
sys_exit(void)
{
  exit();
  return 0;  // not reached
}

int
sys_wait(void)
{
  return wait();
}

int
sys_kill(void)
{
  int pid;

  if(argint(0, &pid) < 0)
    return -1;
  return kill(pid);
}

int
sys_getpid(void)
{
  return myproc()->pid;
}

int
sys_sbrk(void)
{
  int addr;
  int n;

  if(argint(0, &n) < 0)
    return -1;
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

int
sys_sleep(void)
{
  int n;
  uint ticks0;

  if(argint(0, &n) < 0)
    return -1;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(myproc()->killed){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

// return how many clock tick interrupts have occurred
// since start.
int
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

// return how many times a syscall was called by
// a process.
int
sys_getcount(void)
{
    int syscall;
    if (argint(0, &syscall) < 0)
        return -1;
    return myproc() -> syscallcount [syscall - 1];
}

int
sys_v2paddr(void)
{
    // input virtual address
    addr_t *vaddr;
    addr_t *paddr;
    if (argptr(0, (void *) &paddr, sizeof(*paddr)) < 0)
        return -1;
    if (argptr(1, (void *) &vaddr, sizeof(*vaddr)) < 0)
        return -1;
    int a = v2paddr(paddr, vaddr);
    return a;
}
